<h2>About the Cascadia Book Club</h2>
<div class="main about">
    <p>The Cascadia Book Club was founded by Sarah Standish and her husband John Compton in 2014. Sarah had recently moved to Seattle from Portland and was missing the book club she had there. John was hoping to help her settle into life in Seattle and started asking friends if they would be willing to join a book club he was planning to start.</p>
    <p>Sarah's move from Portland to Seattle, as well as John's recent return to the Pacific Northwest from the East Coast, had made John fascinated with the concept of "Cascadia". What unified people in the Pacific Northwest of the United States and the West Coast of Canada? Why did Cascadians who relocated to the East Coast gravitate inexorably toward one another? How difficult should a move between Cascadian cities feel?</p>
    <p>John hoped that the Cascadia Book Club would be a way to explore these questions and help his wife feel more at home in Seattle.</p>
    <p>In 2018, the Cascadia Book Club abandoned the theme of Cascadia, switching to a quarterly rotating theme model. Themes they have covered since then include the Black Death, the Korean Peninsula, and Bogus Tests</p>
